#include <stdio.h>
int main(){
	char letra; //variavel que armazena letra	
	int x; //inteiros
	float salario; //variavel float

	
	//Atribuição entrada 
	letra = 'a';
	x = 10;
	salario = 3972.50;
	
	//Saída de dados na tela
	printf("letra = %c\n",letra);
	printf("x = %d \n",x);
	printf("salario = %2.f",salario);
	
	
	return 0;
}
